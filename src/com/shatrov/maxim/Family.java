package com.shatrov.maxim;

class Family{
    private int humans;
    private int money;
    private int bills;

    public void setHumans(int humans){
        this.humans = humans;
    }

    public int getHumans(){
        return humans;
    }

    public void setMoney(int money){
        this.money = money;
    }

    public int getMoney(){
        return money;
    }

    public void setBills(int bills){
        this.bills = bills;
    }

    public int getBills(){
        return bills;
    }


}
